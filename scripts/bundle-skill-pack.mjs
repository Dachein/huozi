#!/usr/bin/env node
/**
 * Bake skill-pack/ files into src/lib/skill-pack-bundle.ts.
 *
 * Cloudflare Workers has no filesystem at runtime, so the marketplace
 * + plugin tree can't be served by reading files at request time. This
 * script reads each file once at "build authoring time" and emits a TS
 * Record<route-path, { body, type }> that the route handlers import.
 *
 * Re-run after editing anything under skill-pack/ that the public Web
 * surface needs to serve. Wire it into a prebuild step if you forget.
 *
 *   node scripts/bundle-skill-pack.mjs
 */

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const HERE = path.dirname(fileURLToPath(import.meta.url))
const REPO = path.resolve(HERE, '..')
const ROOT = path.join(REPO, 'skill-pack')
const OUT = path.join(REPO, 'src/lib/skill-pack-bundle.ts')

const FILES = [
  ['.claude-plugin/marketplace.json',         'marketplace.json',                                'application/json; charset=utf-8'],
  ['huozi/.claude-plugin/plugin.json',        'huozi/.claude-plugin/plugin.json',                'application/json; charset=utf-8'],
  ['huozi/skills/huozi/SKILL.md',             'huozi/skills/huozi/SKILL.md',                     'text/markdown; charset=utf-8'],
  ['huozi/skills/huozi/REFERENCES.md',        'huozi/skills/huozi/REFERENCES.md',                'text/markdown; charset=utf-8'],
  ['huozi/skills/huozi/templates/deck.html',  'huozi/skills/huozi/templates/deck.html',          'text/html; charset=utf-8'],
  ['huozi/skills/huozi/templates/story.html', 'huozi/skills/huozi/templates/story.html',         'text/html; charset=utf-8'],
  ['huozi/skills/huozi/templates/paper.html', 'huozi/skills/huozi/templates/paper.html',         'text/html; charset=utf-8'],
  ['huozi/skills/huozi/templates/mobile.html','huozi/skills/huozi/templates/mobile.html',        'text/html; charset=utf-8'],
  ['huozi/skills/huozi/templates/page.html',  'huozi/skills/huozi/templates/page.html',          'text/html; charset=utf-8'],
]

const lines = [
  '/**',
  ' * Skill-pack files baked into the Next bundle at build time.',
  ' *',
  ' * Generated by scripts/bundle-skill-pack.mjs from skill-pack/.',
  ' * Do NOT edit by hand — re-run the generator instead.',
  ' *',
  ' * Cloudflare Workers has no fs at runtime, so the route handlers cannot',
  ' * read files from disk. We bake byte-for-byte copies into the JS bundle',
  ' * and serve them from a Record<route-path, body>.',
  ' */',
  '',
  'export interface SkillPackEntry { body: string; type: string }',
  '',
  'export const SKILL_PACK: Record<string, SkillPackEntry> = {',
]

let total = 0
for (const [src, route, type] of FILES) {
  const body = fs.readFileSync(path.join(ROOT, src), 'utf-8')
  total += body.length
  lines.push(`  ${JSON.stringify(route)}: {`)
  lines.push(`    type: ${JSON.stringify(type)},`)
  lines.push(`    body: ${JSON.stringify(body)},`)
  lines.push('  },')
}
lines.push('}')
lines.push('')

fs.mkdirSync(path.dirname(OUT), { recursive: true })
fs.writeFileSync(OUT, lines.join('\n'))

console.log(`Wrote ${path.relative(REPO, OUT)}`)
console.log(`  ${FILES.length} files, ${total} bytes`)
